current_year = 2023  # Assume current year (can be modified if needed)
future_year = 2050  # Target year for calculating age

# Get user input (assume valid integer)
current_age = int(input("How old are you? "))

# Calculate age in future year
age_in_future_year = current_age + (future_year - current_year)

# Print the result
print(f"In {future_year}, you will be {age_in_future_year} years old.")
